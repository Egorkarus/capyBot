module.exports = {
    name: 'help',
    adminOnly: false,
    async execute(message) {
        const commands = message.client.commands;
        if (!commands || commands.size === 0) {
            await message.reply("Не удалось найти доступные команды. 🍊");
            return;
        }

        const descriptions = {
            'join': 'Позвать бота в голосовой канал 👑 *(Только администраторы)*',
            'leave': 'Отключить бота от голосового канала 👑 *(Только администраторы)*',
            'play': 'Включить трек по ссылке YouTube, SoundCloud или прикрепив файл к сообщению 🍊 *(Доступно всем)*',
            'skip': 'Пропустить текущую песню и перейти к следующей в очереди 🍊 *(Доступно всем)*',
            'pause': 'Поставить воспроизведение музыки на паузу 🍊 *(Доступно всем)*',
            'resume': 'Продолжить воспроизведение музыки с паузы 🍊 *(Доступно всем)*',
            'stop': 'Полностью остановить музыку и очистить очередь треков 👑 *(Только администраторы)*',
            'queue': 'Показать список следующих треков в очереди 🍊 *(Доступно всем)*',
            'help': 'Вывести этот список команд и справку по боту 🍊 *(Доступно всем)*'
        };

        const helpText = Array.from(commands.values())
            .map(cmd => `**!${cmd.name}** — ${descriptions[cmd.name] || 'Описание отсутствует.'}`)
            .join('\n');

        await message.reply(`🍊 **Справка по командам Капибары:**\n\n${helpText}`);
    }
};
